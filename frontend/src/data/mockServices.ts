import type { ServicePublic } from "@/types/service.types";

type MockInput = {
  id: string;
  title: string;
  description: string;
  price: number;
  previousPrice?: number;
  tags: string[];
  badge: NonNullable<ServicePublic["badge"]>;
  weeklyHires: number;
  district: string;
};

const now = Date.now();

function buildService(input: MockInput, index: number): ServicePublic {
  const hasFlash = index % 3 === 0;
  return {
    id: input.id,
    title: input.title,
    description: input.description,
    price: input.price,
    previousPrice: input.previousPrice ?? Math.round(input.price * 1.25),
    currency: "PEN",
    coverImageUrl: null,
    isActive: true,
    viewCount: 210 + index * 17,
    tags: input.tags,
    profileId: `profile-${input.id}`,
    userId: `user-${input.id}`,
    createdAt: new Date(now - index * 86_400_000).toISOString(),
    updatedAt: new Date(now - index * 3_600_000).toISOString(),
    profile: {
      displayName: `Freelancer ${index + 1}`,
      avatarUrl: null,
      isAvailable: index % 4 !== 0,
      rating: 4.4 + (index % 5) * 0.1,
      reviewCount: 18 + index * 5,
      responseTimeHours: (index % 4) + 1,
      isVerified: index % 2 === 0,
      district: input.district,
    },
    badge: input.badge,
    weeklyHires: input.weeklyHires,
    etaHours: 12 + (index % 4) * 12,
    distanceKm: 1.8 + (index % 9) * 1.2,
    flashDealEndsAt: hasFlash
      ? new Date(now + (index % 5 + 1) * 3_600_000).toISOString()
      : null,
    remainingSlots: Math.max(1, 8 - (index % 7)),
    soldRatio: 0.45 + (index % 6) * 0.08,
    testimonial:
      index % 2 === 0
        ? "Me resolvió al toque y quedó bravazo."
        : "Volví a contratar en la misma semana.",
  };
}

